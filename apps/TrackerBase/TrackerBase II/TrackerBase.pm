sub HTML
{
	my ($file,$txt);
	my (%hash);
	my $path = shift;
	my @list = ('indexhead2','indexheadb2','indexpost','indexfoot1a','navmap2',
					'tip_head','category_post_title2','category_post_list',
					'indexfoot2','flycast_code',);

	foreach $file (@list)
	{
		undef $txt;
		open FILE, "$path/$file";
		while (<FILE>)
		{
			$txt .= $_;
		}
		close FILE;

		$hash{$file} = $txt;
	}

	return %hash;
}

